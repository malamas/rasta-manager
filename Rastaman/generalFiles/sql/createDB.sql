DROP TABLE PLAYLIST_SONG;
DROP TABLE PLAYLIST;
DROP TABLE SONG;
DROP TABLE ALBUM;
DROP TABLE ARTIST_MUSIC_GROUP;
DROP TABLE Artist;
DROP TABLE MUSIC_GROUP;
DROP TABLE LABEL;
DROP TABLE GENRE;

CREATE TABLE GENRE (
    ID              BIGINT GENERATED BY DEFAULT AS IDENTITY, 
    NAME            VARCHAR(50) NOT NULL, 
    PRIMARY KEY (ID)
);

CREATE TABLE LABEL (
    ID              BIGINT GENERATED BY DEFAULT AS IDENTITY, 
    NAME            VARCHAR(100) NOT NULL, 
    ADDRESS         VARCHAR(100), 
    TELEPHONE_NO    VARCHAR(20), 
    PRIMARY KEY (ID)
);

CREATE TABLE MUSIC_GROUP (
    ID              BIGINT GENERATED BY DEFAULT AS IDENTITY, 
    NAME            VARCHAR(50) NOT NULL, 
    FORMATION_DATE  DATE, 
    PRIMARY KEY (ID)
);

CREATE TABLE Artist (
  ArtistID     bigint GENERATED BY DEFAULT AS IDENTITY, 
  lastName     varchar(50), 
  firstName    varchar(50), 
  artisticName varchar(50), 
  sex          varchar(1) NOT NULL, 
  birthDay     date, 
  birthPlace   varchar(100), 
  musciGenreID BIGINT NOT NULL, 
  PRIMARY KEY (ArtistID),
  FOREIGN KEY (musciGenreID) REFERENCES GENRE (ID) ON DELETE Restrict
);

CREATE TABLE ARTIST_MUSIC_GROUP (
    ARTIST_ID       BIGINT NOT NULL, 
    MUSIC_GROUP_ID  BIGINT NOT NULL, 
    PRIMARY KEY (ARTIST_ID, MUSIC_GROUP_ID),
    FOREIGN KEY (MUSIC_GROUP_ID) REFERENCES MUSIC_GROUP (ID) ON DELETE CASCADE,
    FOREIGN KEY (ARTIST_ID) REFERENCES Artist (ArtistID) ON DELETE RESTRICT
);

CREATE TABLE ALBUM (
    ID              BIGINT GENERATED BY DEFAULT AS IDENTITY, 
    TITLE           VARCHAR(100) NOT NULL, 
    LABEL_ID        BIGINT NOT NULL, 
    RELEASE_DATE    DATE, 
    TYPE            VARCHAR(2) NOT NULL, 
    DISK_NO         INTEGER DEFAULT 1 NOT NULL, 
    ARTIST_ID       BIGINT, 
    MUSIC_GROUP_ID  BIGINT, 
    PRIMARY KEY (ID),
    FOREIGN KEY (MUSIC_GROUP_ID) REFERENCES MUSIC_GROUP (ID) ON DELETE RESTRICT,
    FOREIGN KEY (ARTIST_ID) REFERENCES Artist (ArtistID) ON DELETE RESTRICT,
    FOREIGN KEY (LABEL_ID) REFERENCES LABEL (ID) ON DELETE RESTRICT
);

CREATE TABLE SONG (
    ID              BIGINT GENERATED BY DEFAULT AS IDENTITY, 
    TITLE           VARCHAR(100) NOT NULL, 
    DURATION        INTEGER NOT NULL, 
    TRACK_NO        INTEGER NOT NULL, 
    ALBUM_ID        BIGINT NOT NULL, 
    PRIMARY KEY (ID),
    FOREIGN KEY (ALBUM_ID) REFERENCES ALBUM (ID) ON DELETE CASCADE
);

CREATE TABLE PLAYLIST (
    ID              BIGINT GENERATED BY DEFAULT AS IDENTITY, 
    NAME            VARCHAR(100) NOT NULL, 
    CREATION_DATE   DATE NOT NULL, 
    PRIMARY KEY (ID)
);

CREATE TABLE PLAYLIST_SONG (
    ID              BIGINT GENERATED BY DEFAULT AS IDENTITY,
    PLAYLIST_ID     BIGINT NOT NULL,
    SLOT            INTEGER NOT NULL,    
    SONG_ID         BIGINT NOT NULL, 
    PRIMARY KEY (ID),
    FOREIGN KEY (SONG_ID) REFERENCES SONG (ID) ON DELETE RESTRICT,
    FOREIGN KEY (PLAYLIST_ID) REFERENCES PLAYLIST (ID) ON DELETE CASCADE
);
